name = "Roman"
print(name)  # Roman

# positive indexing printing
print("positive indexing printing:")
print(f"name[0]: {name[0]} ")  # R
print(f"name[1]: {name[1]} ")  # o
print(f"name[2]: {name[2]} ")  # m
print(f"name[3]: {name[3]} ")  # a
print(f"name[4]: {name[4]} ")  # n

# negative indexing printing
print("negative indexing printing:")
print(f"name[-5]: {name[-5]} ")  # n
print(f"name[-4]: {name[-4]} ")  # n
print(f"name[-3]: {name[-3]} ")  # n
print(f"name[-2]: {name[-2]} ")  # n
print(f"name[-1]: {name[-1]} ")  # a

# length of the string
print(f"length of the string: {len(name)}")  # 5

