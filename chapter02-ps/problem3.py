# check the type of varibale that assigned the value through input() function. 

value1 = int(input("Enter a number: "))
print(f"type of value1: {type(value1)}, value1: {value1}")

value2 = str(input("Enter a string: "))
print(f"type of value2: {type(value2)}, value2: {value2}")

value3 = float(input("Enter a decimal number: "))
print(f"type of value3: {type(value3)}, value3: {value3}")